public class IfElse {
    public static void main(String[] args) {
        int a = 14;  
        int b = 14;  

        if (a > b) {  
            System.out.println("a:"+a);      
            System.out.println("b:"+b);   
        }     
        else if (a < b) {  
            System.out.println("b:"+b);   
            System.out.println("a:"+a);      
        }      
        else if (a == b)          
            System.out.println("a:"+a);      

    }
        
}
